
# 算术运算
print(3 + 1)
print(3 - 1)
print(10 / 3)   # 3.3333333333333335
print(10 // 3)  # 3
print(10 % 3)   # 1

print(3 * 4)    # 12
print(3 * 'a')  # aaa
print('a' * 3)  # aaa

print(2 ** 4)
print(2 ** 10)  # 1024

# 赋值运算
a = 2
b = 2
a *= b + 5 # +的优先级大于 *=
print(a)

a = 3
b = 5
print(a > b)
print(a < b)

# 类型转换
print(ord('0'), ord('A'), ord('a'))
print(chr(48), chr(65), chr(97))